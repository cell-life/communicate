package org.celllife.mobilisr.service.exception;

import org.celllife.mobilisr.exception.MobilisrException;

public class DataexportException extends MobilisrException {

	private static final long serialVersionUID = 7549926224375682800L;

	public DataexportException() {
		super();
	}

	public DataexportException(String message, Throwable cause) {
		super(message, cause);
	}

	public DataexportException(String message) {
		super(message);
	}

	public DataexportException(Throwable cause) {
		super(cause);
	}

	
}
