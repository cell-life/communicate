package org.celllife.mobilisr.service.exception;

import org.celllife.mobilisr.exception.MobilisrException;

public class TriggerException extends MobilisrException {

	private static final long serialVersionUID = 8765555955555023889L;

	public TriggerException() {
		super();
	}

	public TriggerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TriggerException(String arg0) {
		super(arg0);
	}

	public TriggerException(Throwable arg0) {
		super(arg0);
	}

	
}
