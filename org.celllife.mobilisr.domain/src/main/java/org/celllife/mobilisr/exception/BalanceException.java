package org.celllife.mobilisr.exception;



public class BalanceException extends MobilisrException {

	private static final long serialVersionUID = 4436268619849300459L;

	public BalanceException() {
		super();
	}

	public BalanceException(String message, Throwable cause) {
		super(message, cause);
	}

	public BalanceException(String message) {
		super(message);
	}

	public BalanceException(Throwable cause) {
		super(cause);
	}
}
