package org.celllife.mobilisr.exception;

public class MobilisrException extends Exception {

	private static final long serialVersionUID = -7013204133472244557L;

	public MobilisrException() {
		super();
	}

	public MobilisrException(String message, Throwable cause) {
		super(message, cause);
	}

	public MobilisrException(String message) {
		super(message);
	}

	public MobilisrException(Throwable cause) {
		super(cause);
	}

	
}
